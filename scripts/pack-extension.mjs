/**
 * Build a Web-Store-ready zip of the extension as a BYOK-only production build.
 *
 * - Copies extension/ to dist/extension/ (the backend/ server is never included).
 * - Rewrites utils/buildConfig.js so DISTILL_INCLUDE_BACKEND = false (no hosted-backend
 *   option in the shipped product).
 * - Zips the build so manifest.json is at the archive root → dist/distill-<version>.zip.
 *
 * Usage: npm run pack
 */
import { cpSync, rmSync, mkdirSync, writeFileSync, readFileSync } from 'fs';
import { execSync } from 'child_process';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const extDir = join(root, 'extension');
const distDir = join(root, 'dist');
const buildDir = join(distDir, 'extension');

const manifest = JSON.parse(readFileSync(join(extDir, 'manifest.json'), 'utf8'));
const version = manifest.version;

// Fresh build dir.
rmSync(buildDir, { recursive: true, force: true });
mkdirSync(distDir, { recursive: true });
cpSync(extDir, buildDir, { recursive: true });

// Force BYOK-only: disable the hosted-backend client in the shipped build.
const prodBuildConfig =
  "'use strict';\n" +
  '// Generated by scripts/pack-extension.mjs — production (BYOK-only) build.\n' +
  'var DISTILL_INCLUDE_BACKEND = false;\n' +
  "if (typeof module !== 'undefined' && module.exports) {\n" +
  '  module.exports = { DISTILL_INCLUDE_BACKEND };\n' +
  '}\n';
writeFileSync(join(buildDir, 'utils', 'buildConfig.js'), prodBuildConfig);

// Zip with manifest at the archive root.
const zipPath = join(distDir, `distill-${version}.zip`);
rmSync(zipPath, { force: true });
try {
  execSync(`cd "${buildDir}" && zip -r -q "${zipPath}" . -x '*.DS_Store'`, { stdio: 'inherit' });
} catch (e) {
  console.error('zip failed (is the `zip` CLI installed?):', e.message);
  process.exit(1);
}

console.log(`Packed BYOK-only build → ${zipPath}`);
console.log('Loaded/zipped from dist/extension/ (backend/ excluded; DISTILL_INCLUDE_BACKEND=false).');
